package Inheritance.Multiple ;

public class Shapes {
    int length ;
    int height ;
    int weight ;
    Shapes(int length ,int height ,int weight){
        this.length = length ;
        this.height = height ;
        this.weight = weight ;
    }
    Shapes(){
        this.length =  -1 ;
        this.height =  -1 ;
        this.weight =  -1 ;
    }
} 
